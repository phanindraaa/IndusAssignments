package com.indus.training.core.domain;

public class AgeCalciOut {
	private int Year;
	private int Month;
	private int Day;
	private int Age;
	
	public void setYear(int d) {
		Year = d;
	}
	public int getYear() {
		return Year;
	}

	public void setMonth(int d) {
		Month = d;
	}
	public int getMonth() {
		return Month;
	}
	
	public void setDay(int d) {
		Day = d;
	}
	public int getDay() {
		return Day;
	}
	
	public void setAge(int d) {
		Age = d;
	}
	public int getAge() {
		return Age;
	}
}
